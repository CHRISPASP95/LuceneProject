package lucene;

public class RelativeDocs {
	
	private String q_id;
	private String q_article;
	
	public RelativeDocs(){
		
	}

	public String getQ_id() {
		return q_id;
	}

	public void setQ_id(String q_id) {
		this.q_id = q_id;
	}

	public String getQ_article() {
		return q_article;
	}

	public void setQ_article(String q_article) {
		this.q_article = q_article;
	}

}
